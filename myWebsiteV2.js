mintButton.addEventListener("click", async function () {
  checkAccount();

  await checkId("0x1");

  const SoulboundBadge = new ethers.Contract(
    "0xd5feC9732115b76F04011821a98E85d5De7BFce5",
    abi,
    signer
  );

  try {
    await SoulboundBadge.publicMint();
  } catch (err) {
    console.log(err.message);
    if (err.message.includes("Only one mint per wallet")) {
      alert("Execution reverted:\nOnly one mint per wallet !");
    }
  }
});

async function checkId(_chainId) {
  const chainId = await provider.getNetwork();
  if (chainId !== _chainId) {
    await ethereum.request({
      method: "wallet_switchEthereumChain",
      params: [{ chainId: _chainId }],
    });
  }
}

function checkAccount() {
  if (typeof signer == "undefined") {
    alert("Please connect to wallet !");
  }
}

